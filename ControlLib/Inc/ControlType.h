#ifndef __CONTROL_TYPE_H__
#define __CONTROL_TYPE_H__
typedef unsigned char       cuint8;  /*  8 bits */
typedef unsigned short int  cuint16; /* 16 bits */
typedef unsigned long int   cuint32; /* 32 bits */
typedef unsigned long long  cuint64; /* 64 bits */

typedef char                cint8;   /*  8 bits */
typedef short int           cint16;  /* 16 bits */
typedef long  int           cint32;  /* 32 bits */
typedef long  long          cint64;  /* 64 bits */

typedef volatile cint8       cvint8;  /*  8 bits */
typedef volatile cint16      cvint16; /* 16 bits */
typedef volatile cint32      cvint32; /* 32 bits */
typedef volatile cint64      cvint64; /* 64 bits */

typedef volatile cuint8      cvuint8;  /*  8 bits */
typedef volatile cuint16     cvuint16; /* 16 bits */
typedef volatile cuint32     cvuint32; /* 32 bits */
typedef volatile cuint64     cvuint64; /* 64 bits */

#endif